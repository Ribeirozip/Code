    private void PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarActionPerformed
           int idPesquisa = Integer.parseInt(IdPesquisa.getText());
           produtoDAO produtoDAO = new produtoDAO();
           Produtos produtos= produtoDAO.getProdutos(idPesquisa);
           if(produtos == null){
           JOptionPane.showMessageDialog(this,"Produto nao encontrado");
           }else{
               txtID.setText(String.valueOf(produtos.getId()));
               nomeprod.setText(produtos.getTitulo());
               desc.setText(produtos.getDescricao());
               preco.setText(Float.toString(produtos.getPrice()));
               quant.setValue(produtos.getQuant());
           }
    }//GEN-LAST:event_PesquisarActionPerformed

