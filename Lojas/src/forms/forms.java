    private void EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarActionPerformed
       String titulo = nomeprod.getText();
       String descricao =desc.getText();
       float price =Float.parseFloat(preco.getText());
       int quantidade = Integer.parseInt(quant.getValue().toString());
       
       Produtos produtos = new Produtos();
       produtos.setTitulo(titulo);
       produtos.setDescricao(descricao);
       produtos.setPrice(price);
       produtos.setQuant(quantidade);
       
       produtoDAO produtoDAO = new produtoDAO();
       produtoDAO.inserir(produtos);
       
       nomeprod.setText("");
       desc.setText("");
       preco.setText("");
       quant.setValue(0);
    }//GEN-LAST:event_EnviarActionPerformed

